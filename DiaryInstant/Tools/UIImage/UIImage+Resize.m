- (UIImage *)getSquareImageWith:(CGFloat)maxWidth
{
    CGRect destImageRect = CGRectMake(0, 0, self.size.width, self.size.height);
    
    CGFloat minLength = MIN(self.size.width, self.size.height);
    
    CGFloat gapLength = self.size.width - self.size.height;
    
    CGFloat offsetLength = ABS(gapLength)/2.f;
    
    CGFloat offsetX = gapLength > 0 ? offsetLength : 0;
    
    CGFloat offsetY = gapLength > 0 ? 0 : offsetLength;
    
    destImageRect = CGRectMake( offsetX, offsetY, minLength, minLength);
    
    CGImageRef imageRef = self.CGImage;
    CGImageRef subImageRef = CGImageCreateWithImageInRect(imageRef, destImageRect);
    CGSize size;
    size.width = CGRectGetWidth(destImageRect);
    size.height = CGRectGetHeight(destImageRect);
    UIGraphicsBeginImageContext(size);
    CGContextRef context = UIGraphicsGetCurrentContext();
    CGContextDrawImage(context, destImageRect, subImageRef);
    UIImage* SquareImage = [UIImage imageWithCGImage:subImageRef];
    CGImageRelease(subImageRef);
    UIGraphicsEndImageContext();
    
    if (SquareImage.size.width > maxWidth)
    {
        destImageRect = CGRectMake( 0, 0, maxWidth, maxWidth);
        
        CGImageRef imageRef = SquareImage.CGImage;
        CGImageRef subImageRef = CGImageCreateWithImageInRect(imageRef, destImageRect);
        size.width = CGRectGetWidth(destImageRect);
        size.height = CGRectGetHeight(destImageRect);
        UIGraphicsBeginImageContext(size);
        CGContextRef context = UIGraphicsGetCurrentContext();
        CGContextDrawImage(context, destImageRect, subImageRef);
        SquareImage = [UIImage imageWithCGImage:subImageRef];
        CGImageRelease(subImageRef);
        UIGraphicsEndImageContext();
    }
    return SquareImage;
}

